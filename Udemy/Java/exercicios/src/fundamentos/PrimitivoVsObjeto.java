package fundamentos;

public class PrimitivoVsObjeto {
	public static void main(String[] args) {
		
		//Tudo em java são objetos menos os tipos primitivos
		
		String s = "texto"; // string s = newString("texto"), 
		System.out.println(s.toUpperCase()); // Tem comportamento (objeto)
		
		int a = 123;
		System.out.println(a); // Não tem comportamento (primitivo)
		
		// Classe Wrappers é a versão objeto dos tipos primitivos
		
		
	}

}
